    private void assignEmpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignEmpBtnActionPerformed
        // TODO add your handling code here:
        int index = allStudentsTbl.getSelectedRow();
        if(index==-1)
            JOptionPane.showMessageDialog(this, "Row not selected", "ERROR", JOptionPane.ERROR_MESSAGE);
        else{
          
          DefaultTableModel dtm = (DefaultTableModel)allStudentsTbl.getModel();
          this.student = (Student)dtm.getValueAt(index, 0);
          idTextField.setText(this.student.getUser_id());
          fullNameTextField.setText(this.student.getFull_name());
          empStatusRadioBtn.setSelected(this.student.getIsEmployed());
          Object selectedItem = dtm.getValueAt(index, 3);
          orgComboBox.setSelectedItem(selectedItem);
          //orgComboBox.setSelectedIndex(index1);

        }
    }//GEN-LAST:event_assignEmpBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        
        this.student.setIsEmployed(empStatusRadioBtn.isSelected());
        Employer emp = (((String)orgComboBox.getSelectedItem()).equals("Not Yet Assigned"))?(null):(this.employerDir.getEmployerFromEmployerId(((String)orgComboBox.getSelectedItem())));
        this.student.setEmployer(emp);
        idTextField.setText("");
        fullNameTextField.setText("");
        empStatusRadioBtn.setSelected(false);
        orgComboBox.setSelectedIndex(0);
        JOptionPane.showMessageDialog(this, "Info Saved Successfully");
        populateTable();
        
    }//GEN-LAST:event_saveBtnActionPerformed


