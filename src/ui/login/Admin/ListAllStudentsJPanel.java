    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)allStudentsTbl.getModel();
        dtm.setRowCount(0);
        for(Student student : this.studentDir.getStudentDirectory()){
            Object[] obj = new Object[4];
            obj[0] = student;
            obj[1] = student.getFull_name();
            obj[2] = String.valueOf(student.getIsEmployed());
            obj[3] = (student.getEmployer()!=null)?(student.getEmployer().getEmp_id()):("Not Yet Assigned");
            dtm.addRow(obj);
        }
    }
