    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel)tblCourseDetails.getModel();
        dtm.setRowCount(0);
        for(Course course : student.getCourseList()){
            Object[] obj = new Object[5];
            obj[0] = course;
            obj[1] = course.getCourseName();
            obj[2] = course.getFaculty().getFull_name();
            obj[3] = student.getTranscript().getGrade().get(String.valueOf(course.getCourseId()));
            obj[4] = student.getTranscript().getCourse_status().get(String.valueOf(course.getCourseId()));
            dtm.addRow(obj);
        }
        
    }

