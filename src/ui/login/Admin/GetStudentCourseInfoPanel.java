    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String gpa = gpaTextField.getText();
        int index = statusComboBox.getSelectedIndex();
        if(index==-1 || gpa.isBlank() || this.course == null){
            JOptionPane.showMessageDialog(this, "Check Course, Course Status or GPA field", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String course_status = statusComboBox.getItemAt(index);
            this.student.getTranscript().setCourse_status(String.valueOf(this.course.getCourseId()),course_status);
            this.student.getTranscript().setGrade(String.valueOf(this.course.getCourseId()), gpa);
            JOptionPane.showMessageDialog(this,"Info Saved Successfully");
        }

        
    }//GEN-LAST:event_saveBtnActionPerformed

    private void viewMoreBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewMoreBtnActionPerformed
        // TODO add your handling code here:
        int index = tblCourseDetails.getSelectedRow();
        if(index==-1){
            JOptionPane.showMessageDialog(this, "Row not selected", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else{
            this.course = (Course)tblCourseDetails.getValueAt(index, 0);
            courseIdTextField.setText(String.valueOf(this.course.getCourseId()));
            courseNameTextField.setText(this.course.getCourseName());
            gpaTextField.setText(this.student.getTranscript().getGrade().get(String.valueOf(this.course.getCourseId())));
            Object selectedItem = tblCourseDetails.getValueAt(index, 4);
            statusComboBox.setSelectedItem(selectedItem);
        }


    }//GEN-LAST:event_viewMoreBtnActionPerformed


