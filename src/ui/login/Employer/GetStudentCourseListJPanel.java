    private void gotoFeedbackPanelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gotoFeedbackPanelBtnActionPerformed
        // TODO add your handling code here:
        int index = courseListTbl.getSelectedRow();
        if(index==-1)
            JOptionPane.showMessageDialog(this, "Row not selected", "ERROR", JOptionPane.ERROR_MESSAGE);
        else
            this.feedbackPanel.setVisible(true);
            DefaultTableModel dtm = (DefaultTableModel)courseListTbl.getModel();
            this.course = (Course)dtm.getValueAt(index, 0);
            this.faculty = this.course.getFaculty();
            this.facNameTextField.setText(this.faculty.getFull_name());
            this.facIdLabel.setText(this.faculty.getFaculty_id());
    }//GEN-LAST:event_gotoFeedbackPanelBtnActionPerformed

    private void sendMsgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendMsgBtnActionPerformed
        // TODO add your handling code here:
            String msg = this.msgArea.getText();
            if(msg.isBlank())
                JOptionPane.showMessageDialog(this, "Feedback cannot be empty","WARNING", JOptionPane.WARNING_MESSAGE);
            else{
                EmpMsgs empMsgs = new EmpMsgs();
                empMsgs.setEmployer(employer);
                empMsgs.setStudent(student);
                empMsgs.setMessage(msg);
                this.faculty.setEmpFeedback(empMsgs);
                JOptionPane.showMessageDialog(this, "Feedback sent successfully");
                facNameTextField.setText("");
                facIdLabel.setText("");
                msgArea.setText("");
                feedbackPanel.setVisible(false);
            }
    }//GEN-LAST:event_sendMsgBtnActionPerformed

