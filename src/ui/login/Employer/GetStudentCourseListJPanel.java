    private void popuateTable() {
        DefaultTableModel dtm = (DefaultTableModel)courseListTbl.getModel();
        dtm.setRowCount(0);
        for(Course course : this.student.getCourseList()){
            String cid = String.valueOf(course.getCourseId());
            if(student.getTranscript().getCourse_status().get(cid).equals("PASS")||
                    student.getTranscript().getCourse_status().get(cid).equals("FAIL")){
                Object[] obj = new Object[3];
                obj[0] = course;
                obj[1] = course.getCourseName();
                obj[2] = course.getFaculty().getFaculty_id();
                dtm.addRow(obj);
            }
        }
    }
